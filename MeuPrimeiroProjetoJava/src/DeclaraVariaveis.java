public class DeclaraVariaveis {
    public static void main(String[] args) {
        //var login= "Olá";
        var password="hbkdghasdhasgdja";
        System.out.println(password);

        /*
        Variáveis tipo:Texto(strings)
        Variaveis tipo:Numeros(int)
        Variaveis tipo: Numeros Fracionários(Double)
        Variaveis tipo: Booleanas(verdadeiro | falso)
         */
        //TIPOS DE DADOS
        //tipo   nomedareferencia  =  conteudo
        String login= "Ola"; //Representação de Texto(ASCII)
        int number1=10; //Representação de Números Inteiros
        double percent=24.30; //Representação de Números fracionários
        boolean isDriver=false; // Representação booleana(estado verdadeiro, falso)

        System.out.println(login);
        System.out.println(number1);
        System.out.println(percent);
        System.out.println(isDriver);

        //variaveis
        // São espaços de memória alocado( Representado por tipos)

        int number; //declarando variavel
        number=55;
        System.out.println(number+10);
    }
}
